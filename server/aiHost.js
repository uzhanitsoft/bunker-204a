function pickRandom(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

export class AIHost {
  constructor(apiKey = null) {
    this.apiKey = apiKey;
    this.useAPI = false; // Will be enabled when API key is provided
  }

  async getWelcome(playerName) {
    if (this.useAPI) return this._callAPI(`Поприветствуй нового игрока ${playerName} с сарказмом`);
    return pickRandom(WELCOME_LINES).replace('{name}', playerName);
  }

  async getGameStart(playerNames) {
    if (this.useAPI) return this._callAPI(`Игра начинается. Игроки: ${playerNames.join(', ')}. Произнеси вступительный монолог.`);
    return pickRandom(GAME_START_LINES);
  }

  async getCardReaction(playerName, cardType, cardValue, profession) {
    if (this.useAPI) {
      return this._callAPI(
        `Игрок ${playerName} открыл карту "${cardType}": "${cardValue}". Его профессия: ${profession}. Прокомментируй с сарказмом.`
      );
    }
    // Try to find profession-specific reaction
    const reactions = CARD_REACTIONS[profession] || CARD_REACTIONS[cardValue];
    if (reactions) return pickRandom(reactions);
    return `${playerName} открывает "${cardValue}". ${cardType === 'Багаж' ? 'Полезность сомнительна.' : 'Интересный выбор для бункера.'}`;
  }

  async getVoteStart(round) {
    if (this.useAPI) return this._callAPI(`Раунд ${round}. Начинается голосование. Подстрекай.`);
    return pickRandom(VOTE_START_LINES);
  }

  async getElimination(playerName, profession) {
    if (this.useAPI) return this._callAPI(`${playerName} (${profession}) вылетает. Попрощайся с сарказмом.`);
    return pickRandom(ELIMINATION_LINES).replace('{name}', playerName);
  }

  async getFinale(winner1, winner2) {
    if (this.useAPI) return this._callAPI(`Финал! Победители: ${winner1} и ${winner2}. Произнеси финальный монолог.`);
    return pickRandom(FINALE_LINES);
  }

  async getScoreComment(playerName, score) {
    const tier = score >= 93 ? 'high' : score >= 88 ? 'mid' : 'low';
    if (this.useAPI) return this._callAPI(`Выставь оценку ${playerName}: ${score} баллов. Прокомментируй.`);
    return pickRandom(SCORE_COMMENTS[tier]).replace('{name}', playerName).replace('{score}', score);
  }

  getPauseText() {
    return PAUSE_TEXT;
  }

  async _callAPI(userMessage) {
    // Placeholder for real Claude API integration
    try {
      const Anthropic = (await import('@anthropic-ai/sdk')).default;
      const client = new Anthropic({ apiKey: this.apiKey });
      const response = await client.messages.create({
        model: 'claude-sonnet-4-20250514',
        max_tokens: 150,
        system: `Ты — ИИ-ведущий игры "Бункер 204-А". Ты хамоватый, саркастичный, с чёрным медицинским юмором. Обращаешься к игрокам по имени. Никогда не помогаешь — только издеваешься. Комментируешь абсурдность ситуации. Отвечай 1-3 предложениями. Только на русском.`,
        messages: [{ role: 'user', content: userMessage }],
      });
      return response.content[0].text;
    } catch (e) {
      console.error('AI API error:', e.message);
      return 'ИИ-ведущий молчит. Видимо, даже ему стало не по себе.';
    }
  }
}
